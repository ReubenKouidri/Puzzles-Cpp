void greet() {
  std::cout << "=============\n";
  std::cout << "UFO: The Game\n";
  std::cout << "=============\n";
  std::cout << "Instructions: save your friend from alien abduction by guessing the letters in the codeword.\n";
}

void end_game(std::string_view answer, std::string_view codeword) {
  if (answer == codeword) {
    std::cout << "Hooray! You saved the person and earned a medal of honor!\n";
  } else {
    std::cout << "Oh no! The UFO just flew away with another person!\n";
  }
}

void display_status(const std::vector<char>& incorrect, const std::string& answer) {
  std::cout << "Incorrect Guesses: \n";
  for (const auto c : incorrect) {
    std::cout << c << " ";
  }
  std::cout << "\n";

  std::cout << "Codeword: \n";
  for (const auto c : answer) {
    std::cout << c << " ";
  }
  std::cout << "\n";
}

